              ></el-option>
            </el-select>
          </div>
        </el-col>
        <el-col :span="6">
          <div class="bar">
            <div class="title">服装层次</div>
            <el-select v-model="searchOptions.searchParams.clothingLevel" clearable>
              <el-option
                v-for="item in searchOptions.options.clothingLevelOptions"
                :key="item.id"
                :label="item.name"
                :value="item.id"
              ></el-option>
            </el-select>
          </div>
        </el-col>
        <el-col :span="6">
          <div class="bar">
            <div class="title">系列名称</div>
            <el-select v-model="searchOptions.searchParams.rangeName" clearable>
              <el-option
                v-for="item in searchOptions.options.rangeNameOptions"
                :key="item.id"
                :label="item.name"
                :value="item.id"
              ></el-option>
            </el-select>
          </div>
        </el-col>
      </el-row>
      <el-row :gutter="20" style="margin-top: 30px; margin-bottom: 5px;">
        <el-col :span="6">
          <div class="bar">
            <div class="title">计划名称</div>
            <el-select v-model="searchOptions.searchParams.name" clearable>
              <el-option
                v-for="item in searchOptions.options.planNameOptions"
                :key="item.name"
                :label="item.name"
                :value="item.name"
              ></el-option>
            </el-select>
          </div>
        </el-col>
        <el-col :span="9">
          <div class="bar">
            <div class="title">添加时间</div>
            <el-date-picker
              style="margin-left: 20px"
              v-model="searchOptions.searchParams.dateRange"
              type="daterange"
              range-separator="至"
              start-placeholde="开始日期"
              end-placeholde="结束日期"
              clearable
            ></el-date-picker>
          </div>
        </el-col>
        <el-col :span="6">
          <el-switch
            v-model="isSelfMadePlan"
            @change="planTypeSwitchChange"
            inactive-color="#13ce66"
            active-text="制定的计划"
            inactive-text="被下发计划">
          </el-switch>
        </el-col>
        <el-col :span="2">
          <el-button type="primary" icon="el-icon-search" @click="handleSearch">搜索</el-button>
        </el-col>
      </el-row>
    </el-card>
    <el-card class="box-card">
      <el-row :gutter="20">
        <el-col :span="3" v-if="isSelfMadePlan">
          <el-button type="primary" size="small" @click="deletePlan" >删除计划</el-button>
        </el-col>
        <el-col :span="3" v-if="isSelfMadePlan">
          <el-button type="primary" size="small" @click="commitPlan" >提交计划</el-button>
        </el-col>
        <el-col :span="3" v-if="!isSelfMadePlan">
          <el-button type="primary" size="small" @click="addPlanChild">添加子计划</el-button>
        </el-col>
        <el-col :span="4" v-if="!isSelfMadePlan">
          <el-button type="primary" size="small" @click="changeOrder">下级计划顺序调整</el-button>
        </el-col>
        <el-col :span="3" v-if="!isSelfMadePlan">
          <el-button type="primary" size="small" @click="addException">添加异常</el-button>
        </el-col>
      </el-row>
      <el-table
        :data="tableData"
        max-height="400"
        @selection-change="changeCheckBoxFun"
        :stripe="true"
        :highlight-current-row="true"
        style="width: 100%; margin-top: 20px"
      >
        <el-table-column width="50" type="selection" align="center"></el-table-column>
        <el-table-column width="50" type="index" label="序号" align="center"></el-table-column>
        <el-table-column prop="id" v-if="false"></el-table-column>
        <el-table-column prop="number" label="计划编号" align="center"></el-table-column>
        <el-table-column prop="name" label="计划名称" align="center"></el-table-column>
        <el-table-column prop="rangeNumber" label="系列编号" align="center"></el-table-column>
        <el-table-column prop="customerName" label="客户名称" align="center"></el-table-column>
        <el-table-column prop="brandName" label="品牌" align="center"></el-table-column>
        <el-table-column prop="rangeName" label="系列名称" align="center"></el-table-column>
        <el-table-column prop="createrName" label="添加人" align="center"></el-table-column>
        <el-table-column prop="deptName" label="部门" align="center"></el-table-column>
        <el-table-column prop="createTime" label="添加时间" align="center"></el-table-column>
        <el-table-column prop="parentName" label="上级计划" align="center"></el-table-column>
        <el-table-column prop="havePlan" label="状态" align="center">
          <template slot-scope="scope">
            <p v-if="scope.row.havePlan">已制定</p>
            <p v-else>未制定</p>
          </template>
        </el-table-column>
        <el-table-column label="异常状态" width="150" align="center">
          <template slot-scope="scope">
            <el-button
              @click.native.prevent="ToSearchException(scope.row)"
              type="text"
              size="small"
              v-if="scope.row.haveException"
            >有异常，查看</el-button>
            <p v-else>无异常</p>
          </template>
        </el-table-column>
        <el-table-column fixed="right" label="操作" width="150" align="center">
          <template slot-scope="scope">
            <el-button @click.native.prevent="getPlanDetail(scope.row)" type="text" size="small">查看</el-button>
            <el-button
              v-if="isSelfMadePlan"
              @click.native.prevent="ModifyPlanDetail(scope.row)"
              type="text"
              size="small"
            >修改</el-button>
            <el-button
              v-if="isSelfMadePlan"
              @click.native.prevent="deleteOnePlan(scope.row.id)"
              type="text"
              size="small"
            >删除</el-button>
          </template>
        </el-table-column>
      </el-table>
      <div class="block">
        <el-pagination
          @size-change="handleSizeChange"
          @current-change="handleCurrentChange"
          :current-page.sync="pagination.currentPage"
          :page-sizes="pagination.pageSizes"
          :page-size="pagination.pageSize"
          layout="total, sizes, prev, pager, next, jumper"
          :total="pagination.total"
        ></el-pagination>
      </div>
    </el-card>
  </div>
</template>

<script>
import { error } from 'util';
export default {
  data() {
    return {
      isSelfMadePlan:false,
      searchOptions: {
        searchParams: {
          customerName: "",
          brandName: "",
          clothingLevel: "",
          rangeName: "",
          name:"",
          dateRange: ""
        },
        options: {
          customerNameOptions: [],
          brandNameOptions: [],
          clothingLevelOptions: [],
          rangeNameOptions:[],
          planNameOptions:[],
        }
      },
      tableData: [],
      pagination: {
        currentPage: 1,
        pageSizes: [5, 10, 20, 30, 50],
        pageSize: 5,
        total: 400
      },
      selectedData: []
    };
  },
  created: function () {
    const that = this;
    console.log('进入计划管理页面');

    //获取客户名称
    this.$axios
      .get(`${window.$config.HOST}/baseInfoManagement/getCustomerName`)
      .then(response => {
        this.searchOptions.options.customerNameOptions = response.data;
      })
      .catch(error => {
        console.log("初始化客户选项错误!");
      });

    //获取服装层次
    that.$axios
      .get(`${window.$config.HOST}/baseInfoManagement/getClothingLevelName`)
      .then(response => {
        this.searchOptions.options.clothingLevelOptions = response.data;
      })
      .catch(error => {
        console.log("初始化服装层次加载错误");
      });

    //品牌名称选择获取
    this.$axios
      .get(`${window.$config.HOST}/baseInfoManagement/getBrandName`,{
        params:{customerId:""}
      })
      .then(response => {
        this.searchOptions.options.brandNameOptions = response.data;
      })
      .catch(error => {
        console.log("初始化品牌名称选择错误");
      });

    //初始化获取系列
    this.$axios
      .get(`${window.$config.HOST}/infoManagement/getRangeName`,{
        params:{brandId:""}
      })
      .then(response => {
        this.searchOptions.options.rangeNameOptions = response.data;
      })
      .catch(error => {
        console.log("初始化系列名称加载错误");
      });

    //默认获取被下发计划列表
    var param = {
      stage: "manage"
    }
    this.$axios
      .get(`${window.$config.HOST}/planManagement/getDistributedPlanList`)
      .then(response => {
        this.tableData = response.data;
        this.searchOptions.options.planNameOptions = response.data;
      })
      .catch(error => {
        console.log("初始化被下发计划列表获取错误");
        // this.tableData = [
        //   {
        //     id: 1,
        //     number: "00001",
        //     name: "1",
        //     rangeNumber: "1",
        //     customerName: "1",
        //     brandName: "1",
        //     rangeName: "1",
        //     createrName: "1",
        //     deptName: "1",
        //     createTime: "2019-4-9",
        //     parentId: "无",
        //     havePlan: true,
        //     haveException: true
        //   },
        //   {
        //     id: 2,
        //     number: "00002",
        //     name: "2",
        //     rangeNumber: "2",
        //     customerName: "2",
        //     brandName: "2",
        //     rangeName: "2",
        //     createrName: "2",
        //     deptName: "2",
        //     createTime: "2019-4-9",
        //     parentId: "无",
        //     havePlan: true,
        //     haveException: true
        //   },
        // ];
      });
  },
  methods: {
    ToSearchException(row) {
      console.log("查看异常"+row.id);
      this.$router.push({
        name: "exceptionManagement",
        params: {
          planId: row.id,
          customerId: row.customerId,
          customerName: row.customerName,
          brandId: row.brandId,
          brandName: row.brandName,
          rangeId: row.rangeId,
          rangeName: row.rangeName,
        }
      });
    },
    addPlanChild() {
      const that = this;
      if (that.selectedData.length === 1) {
        let data = that.selectedData[0];
        that.$router.push({
          name: "planMakeIndex",
          params: {
            client: data.customerName,
            brand: data.brandName,
            series: data.rangeName,
            plantype: 2,
            planobj: data.rangeName
          }
        });
      } else if (that.selectedData.length === 0) {
        that.$message.error("请选择要添加子计划的计划！");
      } else {
        that.$message.error("仅允许对一条计划添加子计划，请重新选择！");
      }
    },
    deletePlan() {
      const that = this;
      if (that.selectedData.length === 0) {
        that.$message.error("请选择要删除的计划！");
      } else {
        this.selectedData.forEach(element=>{
            console.log(element.id);
            this.$axios
              .delete(`${window.$config.HOST}/planManagement/deletePlan`,{
                params:{id:element.id}
              })
              .then(response=>{
                if(response.data < 0 ){
                  this.$message.error(element.id + "删除失败!");
                }else{
                  this.$message({
                    type:"success",
                    message: element.id+"删除成功!"
                  });
                  this.handleSearch()
                }
              })
              .catch(error=>{
                this.$message.error(element.id + "删除失败!");
              })
          });
      }
    },
    commitPlan(){
      if (that.selectedData.length === 0) {
        that.$message.error("请选择要提交的计划！");
      } else {
        this.selectedData.forEach(element=>{
            console.log(element.id);
            this.$axios
              .get(`${window.$config.HOST}/planManagement/submitPlan`,{
                params:{id:element.id}
              })
              .then(response=>{
                if(response.data < 0 ){
                  console.log(element.id + "提交失败!");
                  this.$message.error(element.id + "提交失败!");
                }else{
                  this.$message({
                    type:"success",
                    message: element.id+"提交成功!"
                  });
                  this.handleSearch()
                }
              })
              .catch(error=>{
                console.log(element.id + "提交失败!");
                this.$message.error(element.id + "提交失败!");
              })
          });
      }
    },
    changeOrder() {
      this.$message("此功能对应页面暂时缺失");
    },
    addException() {
      const that = this;
      if (that.selectedData.length === 0) {
        that.$message.error("请选择要添加异常的计划！");
      } else {
        that
          .$prompt("请输入计划出现的异常", "异常信息添加", {
            confirmButtonText: "确定",
            cancelButttonText: "取消",
            inputPattern: /\S/,
            inputErrorMessage: "异常内容不得为空"
          })
          .then(({ value }) => {
            console.log(value);
            that.selectedData.forEach(element=>{
              var params = {
                planId : element.id,
                cause : (value==="")?"":value,
              };
              // console.log(params);
              that.$axios.post(`${window.$config.HOST}/planManagement/addException`,params)
                .then(response=>{
                  if(response.data < 0){
                    that.$message.error(element.name + "添加异常失败");
                  }else{
                    that.$message({
                      type:"success",
                      message:element.name+"异常添加成功"
                    })
                  }
                })
                .catch(error=>{
                  that.$message.error(element.name + "添加异常失败");
                })
            });
          });
      }
    },
    changeCheckBoxFun(val) {
      this.selectedData = val;
    },
    getPlanDetail(row) {
      const that = this;
      that.$router.push({
        name: "planMakeIndex",
        params: {
          flag: 4,
          goback: "planManagement",
          client: row.customerName,
          brand: row.brandName,
          series: row.rangeName,
          plantype: 2,
          planobj: row.rangeName
        }
      });
    },
    ModifyPlanDetail(row) {
      const that = this;
      that.$router.push({
        name: "planMakeIndex",
        params: {
          goback: "planManagement",
          flag: 5,
          client: row.customerName,
          brand: row.brandName,
          series: row.rangeName,
          plantype: 2,
          planobj: row.rangeName
        }
      });
    },
    deleteOnePlan(planid) {
      console.log("删除 "+planid);
      this.$axios.post(`${window.$config.HOST}/planManagement/deletePlan`,{
        params:{id:planid}
      })
        .then(response=>{
          if(response.data < 0){
            this.$message.error(planid+"删除失败");
            return;
          }else{
            this.handleSearch();
            this.$message({
              type:"success",
              message:planid+"删除成功"
            });
          }
        })
        .catch(error=>{
          this.$message.error(planid+"删除失败");
        });
    },
    // 改变日期格式
    changeDate(date) {
      if(!date){
        return undefined;
      }else{
        var y = date.getFullYear();
        var m = date.getMonth() + 1;
        m = m < 10 ? "0" + m : m;
        var d = date.getDate();
        d = d < 10 ? "0" + d : d;
        // var h = date.getHours();
        // var minute = date.getMinutes();
        // minute = minute < 10 ? "0" + minute : minute;
        // var second = date.getSeconds();
        // second = minute < 10 ? "0" + second : second;
        // return y + "-" + m + "-" + d + " " + h + ":" + minute + ":" + second;
        return y + "-" + m + "-" + d;
      }
    },
    //搜索按钮
    handleSearch(){
      var param = {
        customerId: (this.searchOptions.searchParams.customerName==="")?undefined:this.searchOptions.searchParams.customerName, 
        brandId: (this.searchOptions.searchParams.brandName==="")?undefined:this.searchOptions.searchParams.brandName, 
        rangeId: (this.searchOptions.searchParams.rangeName==="")?undefined:this.searchOptions.searchParams.rangeName,  
        name: (this.searchOptions.searchParams.name==="")?undefined:this.searchOptions.searchParams.name, 
        clothingLevelId :(this.searchOptions.searchParams.name==="")?undefined:this.searchOptions.searchParams.name, 
        startDate: this.changeDate(this.searchOptions.searchParams.dateRange[0]),
        endDate:this.changeDate(this.searchOptions.searchParams.dateRange[1]),
      };

      if(this.isSelfMadePlan){
        param.stage = "manage";
        console.log(param);
        this.$axios
          .get(`${window.$config.HOST}/planManagement/getPlanList`,{
            params:param
          })
          .then(response=>{
            this.tableData = response.data;
          })
          .catch(error=>{
            this.$message.error("搜索失败!");
          });
      }else{
        console.log(param);
        this.$axios
          .get(`${window.$config.HOST}/planManagement/getDistributedPlanList`,{
            params:param
          })
          .then(response=>{
            this.tableData = response.data;
          })
          .catch(error=>{
            this.$message.error("搜索失败!");
          });
      }
    },
    planTypeSwitchChange(){
      this.tableData = [];
      if(this.isSelfMadePlan){
        this.$axios
          .get(`${window.$config.HOST}/planManagement/getPlanList`,{
            params:{stage:"manage"}
          })
          .then(response=>{
            this.tableData = response.data;
          })
          .catch(error=>{
            this.$message.error("搜索失败!");
          });
      }else{
        this.$axios
          .get(`${window.$config.HOST}/planManagement/getDistributedPlanList`)
          .then(response=>{
            this.tableData = response.data;
          })
          .catch(error=>{
            this.$message.error("搜索失败!");
          });
      }
    },
    handleSizeChange(){

    },
    handleCurrentChange(){
      
    }
  }
};
</script>

